class Program10{
    public static void main(String[] args){
 
        // a. The number of bits used to represent a char value
        int bits = 16;
        System.out.println("Number of bits used to represent a char value: "+bits);
 
        // b. The number of bytes used to represent a char value
        int bytes = 2;
        System.out.println("Number of bytes used to represent a char value: "+bytes);
 
        // c. The minimum value a char
        char minChar = 0;
        System.out.println("Minimum value a char is: "+minChar);
 
        // d. The maximum value a char
        char maxChar = 65535;
        System.out.println("Maximum value a char is: "+maxChar);
    }
}
    
