class Program7{
    public static void main(String [] args){

		// a. The number of bits used to represent a byte value
		int numBits = 8;
		System.out.println("The number of bits used to represent a byte value: " + numBits);
		
		// b. The number of bytes used to represent a byte value
		int numBytes = 1;
		System.out.println("The number of bytes used to represent a byte value: " + numBytes);
		
		// c. The minimum value a byte
		int minValue = -128;
		System.out.println("The minimum value of a byte: " + minValue);
		
		// d. The maximum value a byte
		int maxValue = 127;
		System.out.println("The maximum value of a byte: " + maxValue);
	}
}
        
    